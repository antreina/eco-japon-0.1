                COMO SE TRATAN LOS RAEE
              </h3>
              <h5 class="fw-medium">
                Los RAEE (Residuos de Aparatos Eléctricos y Electrónicos) se tratan mediante un proceso de 
                reciclaje que implica varias etapas. En primer lugar, se realiza una selección y clasificación 
                de los diferentes tipos de materiales, como metales, plásticos, vidrios y circuitos electrónicos. 
                Luego, se desmontan los productos para separar los componentes, y se eliminan los materiales 
                tóxicos y peligrosos, como baterías y condensadores. <br><br>
                Después de la separación de materiales, cada tipo de residuo se procesa individualmente. 
                Por ejemplo, los metales se funden y se adaptan en nuevas piezas metálicas, mientras que 
                los plásticos se trituran y se utilizan en la fabricación de nuevos productos plásticos. 
                Los circuitos electrónicos se trituran y se separan en sus componentes para su reutilización. <br> <br>              
                En algunos casos, los productos electrónicos todavía están en buen estado y se pueden reparar 
                y reutilizar en su forma original o en otras aplicaciones. En estos casos, los productos se 
                someten a pruebas y reparaciones para garantizar su funcionamiento y seguridad antes de ser revendidos o donados.<br><br>               
                En resumen, el tratamiento de los RAEE implica una serie de procesos de separación y 
                reciclaje que permiten recuperar materiales valiosos, minimizar la contaminación y evitar la pérdida de recursos.
