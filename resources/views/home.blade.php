                IMPORTANCIA DEL RECICLAJE DE RESIDUOS TECNOLÓGICOS
              </h3>
              <h5 class="fw-medium">
                1. Protege la salud pública: La exposición a sustancias tóxicas y peligrosas en los residuos 
